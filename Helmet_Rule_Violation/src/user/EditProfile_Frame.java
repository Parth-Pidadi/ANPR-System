    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
          String register_as=jTextField6.getText();
        String name=jTextField1.getText();
        String mobile=jTextField2.getText();
        String email=jTextField3.getText();
        String username=jTextField4.getText();
        char pass[]=jPasswordField1.getPassword();
        String password=String.valueOf(pass);
        char con[]=jPasswordField2.getPassword();
        String conpass=String.valueOf(con);
        String id=jTextField5.getText();
        
        
         if(name.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Enter Name");
        else if(!isValidMobile(mobile))
            JOptionPane.showMessageDialog(null," Please Enter Valid Mobile Number");
        else if(!isValidEmail(email))
            JOptionPane.showMessageDialog(null, "Please Enter Valid Email Id");
        else if(username.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Enter Username");
        else if(password.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Enter Password");
        else if(conpass.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Re-Enter Password");
        else if(!password.equals(conpass))
                JOptionPane.showMessageDialog(null, "Password And Confirm Password Is Not Matched");
        else if(id.isEmpty())
            JOptionPane.showMessageDialog(null, "Please Get The Alphanumeric Id");
         else
        {
            EditProfile ep=new EditProfile();
            if(ep.doEdit(register_as, name, mobile, email, username, password, id))
                JOptionPane.showMessageDialog(null, register_as+"Information Updated Successfully");
            else
                JOptionPane.showMessageDialog(null, "Error In "+register_as+" Information Updation");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

